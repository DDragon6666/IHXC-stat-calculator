def do_addition_artifact(stats, artifact_name):
    artifact = ARTIFACTS.get(artifact_name)
    _do_addition_artifact(stats, artifact)

def _do_addition_artifact(stats, artifact): # goes through the artifact and adds the stats
    for i in artifact:
        if i[:7] == 'faction':
            if i[8:] == stats.get('faction'):
                _do_addition_artifact(stats, artifact.get(i))
                continue
            else:
                continue
        if i[:7] == 'percent': continue
        stats[i] += artifact.get(i)

def do_artifact_percents(stats, artifact_name):
    artifact = ARTIFACTS.get(artifact_name)
    _do_artifact_percents(stats, artifact)

def _do_artifact_percents(stats, artifact):
    for i in artifact:
        if i[:7] == 'faction':
            if i[8:] == stats.get('faction'):
                _do_artifact_percents(stats, artifact.get(i))
                continue
            else:
                continue
        if not(i[:7] == 'percent'): continue
        stat = i[8:]
        stats[stat] *= 1+artifact.get(i)/100
        stats[stat] = int(stats.get(stat))